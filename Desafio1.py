# Código para fazer a soma de dois números
# Será solicitado para o usuário que ele digite os dois números 
# O float é usado para representar números quebrados 
# O input é usado para interpretar os números e no geral o que a pessoa digitar 
numero1 = float(input("Escreva o primeiro número: "))
numero2 = float(input("Escreva o segundo número: "))

# Será calculada a soma dos dois números que o usuário escreveu
soma = numero1 + numero2

# O resultado da soma será apresentado
# O print é usado para mostrar o resultado do código
print("A soma dos dois números é:", soma)
